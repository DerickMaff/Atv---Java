package Atividades2;

public class variaveis5 {
    public static void main(String[] args ){

        /* Declara uma variavel e incrementar 3 vezes e mostras as 3 vezes incrementadas ( com o '++' )*/
        int valorIncrementado = 0;

        valorIncrementado++;
        System.out.println("primeiro "+valorIncrementado);

        valorIncrementado++;
        System.out.println("Segundo "+ valorIncrementado);

        valorIncrementado++;
        System.out.println("Terceiro "+ valorIncrementado);

    }
}